import streamlit as st

# 页面配置
st.set_page_config(
    page_title="人工光照与LED灯知识库",
    page_icon="💡",
    layout="wide"
)

# 创建标签页
tabs = st.tabs(["LED灯原理", "光强与光配方参数", "市面上的产品"])

# LED灯原理
with tabs[0]:
    st.title("LED灯原理")
    st.write("""
        LED（Light-Emitting Diode，发光二极管）是一种半导体器件，具有发光特性。LED灯的工作原理是通过注入正负载流子，使得电子和空穴复合释放出能量，产生光子。以下是LED灯的特点和优势：

        - **高效节能**：LED灯具有高效的光电转换效率，能够将大部分电能转化为光能。
        - **寿命长**：LED灯具有较长的使用寿命，通常可达数万小时。
        - **色温可调**：通过控制LED的发光材料和电流，可以调节LED灯的色温和色彩。

        LED灯已广泛应用于照明、显示屏和光通信等领域，是现代照明技术的主流之一。
    """)

# 光强与光配方参数
with tabs[1]:
    st.title("光强与光配方参数")
    st.write("""
        LED灯的光强和光配方参数是评估其照明性能的重要指标：

        - **光强**：LED灯的光强表示单位面积上的光通量，通常以流明/平方米（lm/m²）或勒克斯（lux，lx）来衡量。
        - **光配方参数**：包括色温、色彩指数（CRI）、光谱分布等，影响LED灯的光色和光质。

        光强和光配方参数的选择取决于照明需求和应用场景，例如植物生长、室内照明等。
    """)

