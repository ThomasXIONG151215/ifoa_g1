# Rhino介绍
with tabs[1]:
    st.title("Rhino介绍")
    st.write("""
        Rhino（犀牛）是一款专业的三维建模工具，广泛应用于建筑设计、工业设计、航空航天等领域。它具有以下特点：

        - **强大的建模功能**：Rhino支持多种曲面和实体建模技术，可以创建复杂的几何形体。
        - **灵活的插件系统**：Rhino拥有丰富的插件库，如Grasshopper、V-Ray等，扩展了其功能和应用范围。
        - **跨平台支持**：Rhino可以在Windows和Mac操作系统上运行，满足不同用户的需求。

        Rhino是建筑师、设计师和艺术家们的首选工具之一，用于实现创意设计和复杂建模任务。
    """)

# Grasshopper介绍
with tabs[2]:
    st.title("Grasshopper介绍")
    st.write("""
        Grasshopper（草图板）是Rhino的视觉编程语言插件，用于生成参数化设计和算法建模。它具有以下特点：

        - **可视化编程**：Grasshopper使用图形界面进行编程，无需编写代码，使复杂的算法设计变得直观易懂。
        - **参数化设计**：通过调整参数，快速生成不同形式和结构的设计方案，支持快速迭代和优化。
        - **集成Rhino生态系统**：Grasshopper与Rhino无缝集成，可以直接操作Rhino中的几何对象和数据。

        Grasshopper适用于建筑师、工程师和设计师，是实现参数化设计和算法驱动建模的重要工具。
    """)
