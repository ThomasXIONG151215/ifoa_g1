import streamlit as st

# 页面配置
st.set_page_config(
    page_title="水泵知识库",
    page_icon="🌊",
    layout="wide"
)

# 创建标签页
tabs = st.tabs(["水泵参数", "选型方法", "市场上的选择"])

# 水泵参数
with tabs[0]:
    st.title("水泵参数介绍")
    st.write("""
        水泵是一种用于输送液体的机械设备，常用于供水、排水、循环和增压等应用。以下是水泵的常见参数：

        - **流量（Q）**：单位时间内通过水泵的液体体积，通常以立方米/小时（m³/h）或升/分钟（L/min）表示。
        - **扬程（H）**：液体从入口到出口所需的压力高度，通常以米（m）表示。
        - **功率（P）**：水泵的功率，用于推动液体流动，通常以千瓦（kW）表示。
        - **效率（η）**：水泵的能量转换效率，表示输出功率与输入功率之比。

        这些参数是评估水泵性能和选择合适水泵的重要指标。
    """)

# 选型方法
with tabs[1]:
    st.title("水泵选型方法")
    st.write("""
        选择合适的水泵需要考虑以下因素：

        1. **流量和扬程需求**：根据具体应用确定所需的流量和扬程。
        2. **工作条件**：考虑液体性质、温度、压力等工作条件。
        3. **效率和能耗**：选择高效率的水泵，降低能源消耗成本。
        4. **可靠性和维护**：考虑水泵的可靠性和维护成本。
        5. **安装和使用环境**：考虑水泵的安装位置、使用环境和空间限制。

        可以根据需求使用水泵选型软件或咨询专业工程师进行合适水泵的选型。
    """)

# 市场上的选择
with tabs[2]:
    st.title("市场上的水泵选择")
    st.write("""
        市场上有各种类型和品牌的水泵产品，涵盖了不同应用和性能要求：

        - **离心泵**：常用于大流量、中低扬程的输送。
        - **柱塞泵**：适用于高压力、小流量的输送。
        - **自吸泵**：适用于无法建设吸入管的场合，可自吸液体。
        - **潜水泵**：用于潜水应用，如排水、污水处理等。
        - **消防泵**：用于消防系统，具有高流量和高扬程。

        在选择市场上的水泵时，需要根据具体需求和应用场景进行评估和比较，考虑性能、质量、售后服务等因素。
    """)
