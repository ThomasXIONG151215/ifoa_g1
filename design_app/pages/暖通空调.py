# 基本概念
with tabs[0]:
    st.title("暖通空调基本概念")
    st.write("""
        暖通空调（HVAC）是指供热、通风和空调系统，用于调节室内环境的温度、湿度、空气质量等参数，提供舒适的室内环境条件。以下是暖通空调的基本概念和要点：

        - **供热系统**：通过锅炉、热泵或电热器等设备提供室内热量，保持室内温度适宜。
        - **通风系统**：通过空气处理机、风机等设备进行新风换气，保持室内空气新鲜。
        - **空调系统**：通过制冷剂循环、蒸发冷却等方式调节室内温度和湿度，提供舒适的室内环境。
        - **空气质量控制**：包括过滤、除湿、除臭等措施，提高室内空气质量。
        
        暖通空调系统是建筑中重要的设施之一，影响着室内环境舒适度和能源利用效率。
    """)

# 常见空调类型
with tabs[1]:
    st.title("常见空调类型")
    st.write("""
        常见的空调类型包括：

        - **中央空调系统**：通过集中供冷供热，通过管道和风管向整个建筑传输冷热空气。
        - **分体式空调**：包括单元式空调和多联机式空调，适用于单个房间或小空间。
        - **VRF空调系统**：变频多联式空调系统，适用于大型建筑，可实现灵活控制和节能运行。
        - **地源热泵系统**：利用地热能源进行供热供冷，具有高效节能的特点。
        
        不同类型的空调系统具有各自的特点和适用范围，可根据建筑需求和能源要求进行选择。
    """)

# 负荷统计模型
with tabs[2]:
    st.title("暖通空调负荷统计模型")
    st.write("""
        暖通空调负荷统计模型用于评估建筑热负荷和冷负荷，确定空调系统的设计容量和运行需求。常见的负荷统计模型包括：

        - **简化法**：根据建筑尺寸、材料、朝向等因素估算热负荷和冷负荷。
        - **详细法**：利用建筑能耗模拟软件，如EnergyPlus、TRACE等，进行详细的热负荷分析和模拟。

        负荷统计模型可以帮助设计师和工程师确定合适的空调系统配置，提高能源利用效率和室内舒适度。
    """)

